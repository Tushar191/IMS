    public void feerecord(){
         DefaultTableModel model =(DefaultTableModel)jTable1.getModel() ;

        try{
            Class.forName("java.sql.Driver") ;
Connection con = DriverManager.getConnection("jdbc:mysql://localhost/institute","root","123");
            Statement stmt = con.createStatement();
            String query ="select distinct * From fee order by student_id" ;
            ResultSet rs = stmt.executeQuery(query);

            while(rs.next()) {
            String dno =rs.getString("Student_id");
            String dname =rs.getString("Student_name") ;
             String lc =rs.getString("Father_name") ;
              String rd =rs.getString("class") ;
           int as =rs.getInt("Amount") ;
           java.util.Date dob=rs.getDate("date");
          int aml=rs.getInt("amount_left");

              model.addRow(new Object[] {dno ,dname, lc, rd, as ,dob,aml});
            }
            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }

